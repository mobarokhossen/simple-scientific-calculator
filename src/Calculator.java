    private void equalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equalActionPerformed
        
        
       try{ 
       
        second = (Double.parseDouble(String.valueOf(display.getText())));
        
        //       plus implementatiion
        
        if(plusClick>0){
            total= first+second;
            display.setText((String.valueOf(total)));
            first = 0;
            second = 0;
            plusClick = 0;
            equalClick=1;
            displayError.setText("");
        }
        //       minus implementatiion
        
          if(minusClick>0){
            total= first-second;
            display.setText((String.valueOf(total)));
            first = 0;
            second = 0;
            minusClick = 0;
            equalClick=1;
            displayError.setText("");
            
        }
          //      multiply implementatiion
          
            if(multiplyClick>0){
            total= first*second;
            display.setText((String.valueOf(total)));
            first = 0;
            second = 0;
            multiplyClick = 0;
            equalClick=1;
            displayError.setText("");
        }
            //    Divide implementatiion
            
              if(divideClick>0){
            total= first/second;
            display.setText((String.valueOf(total)));
            first = 0;
            second = 0;
            divideClick = 0;
            equalClick=1;
            displayError.setText("");
        }
              //    power implementatiion
              
              if(powerClick>0){
                  total = Math.pow(first,second);
                  display.setText((String.valueOf(total)));
                  first=0;
                  second=0;
                  powerClick=0;
                  equalClick=1;
                  displayError.setText("");
              }
              if(percentageClick>0){
                  total = (first*second)/100;
                  display.setText((String.valueOf(total)));
                  first=0;
                  second=0;
                  percentageClick=0;
                  equalClick=1;
                  displayError.setText("");
              }
       }catch(Exception e){
           displayError.setText("Don't Double Click the Operator");
        }
              
    }//GEN-LAST:event_equalActionPerformed

